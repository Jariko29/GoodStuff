import math

# Δεδομένα
R = 1.0  # Εσωτερική αντίσταση του πηνίου σε ohms
L = 400e-3  # Αυτεπαγωγή του πηνίου σε henrys
C = 2.0e-6  # Χωνευτικότητα του πυκνωτή σε farads
V0 = 24.0  # Αρχική τάση στο πηνίο σε volts

# (α) Αρχικά τάση στο πηνίο
V_initial = V0

# (β) Ενέργεια που χάνεται
omega = math.sqrt(1 / (L * C) - R**2 / (4 * L**2))
I = V0 / math.sqrt(L**2 * (1 / (L * C) - R**2 / (4 * L**2)))
E_lost = 0.5 * L * I**2

# (γ) Συχνότητα των ταλαντώσεων
frequency = omega / (2 * math.pi)

# (δ) Παράγοντας ποιότητας
Q_factor = math.sqrt(L / C) / R

# Εκτύπωση αποτελεσμάτων
print("(α) Αρχικά τάση στο πηνίο:", V_initial, "V")
print("(β) Ενέργεια που χάνεται:", E_lost, "Joules")
print("(γ) Συχνότητα των ταλαντώσεων:", frequency, "Hz")
print("(δ) Παράγοντας ποιότητας:", Q_factor)
